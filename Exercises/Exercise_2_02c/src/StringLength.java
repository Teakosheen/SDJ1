
public class StringLength
{

   public static void main(String[] args)
   {
      
      
     String littleName = "herman";
     String bigName = littleName.toUpperCase();
     
     int stringSize;
     stringSize = bigName.length();
    
     System.out.println(bigName + " has " + stringSize +
                            " characters.");
     

   }

}
